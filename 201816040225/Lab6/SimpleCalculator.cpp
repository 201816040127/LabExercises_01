#include"SimpleCalculator.h"


double SimpleCalculator::add(double a,double b)const//add function
{
    return a+b;
}
double SimpleCalculator::subtract(double a,double b)const//subtract function
{
    return a-b;
}
double SimpleCalculator::multiply(double a,double b)const//multiply function
{
    return a*b;
}
double SimpleCalculator::divide(double a,double b)const//divide function
{
    return a/b;
}
